package gov.geoplatform.uasdm.odm;

public class UnreachableHostException extends UnreachableHostExceptionBase
{
  public static final long serialVersionUID = -1527022276;
  
  public UnreachableHostException()
  {
    super();
  }
  
  public UnreachableHostException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public UnreachableHostException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public UnreachableHostException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}

package gov.geoplatform.uasdm.bus;

public class DuplicateSiteException extends DuplicateSiteExceptionBase
{
  private static final long serialVersionUID = -1715465549;
  
  public DuplicateSiteException()
  {
    super();
  }
  
  public DuplicateSiteException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public DuplicateSiteException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public DuplicateSiteException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
